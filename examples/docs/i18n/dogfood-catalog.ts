function dogfoodMessage(
  id: string,
  value: string,
  translations: Readonly<Record<string, string>> = {},
) {
  return {
    key: { namespace: DOGFOOD_I18N_NAMESPACE, id },
    kind: 'message' as const,
    sourceLocale: 'en',
    values: { en: value, ...translations },
  };
}
